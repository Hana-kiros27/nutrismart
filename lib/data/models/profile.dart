class Profile {

  String username;

  int age;
  double weight;
  double height;

  String gender;
  String activityLevel;
  String goal;

  Profile({
    required this.username,
    required this.age,
    required this.weight,
    required this.height,
    required this.gender,
    required this.activityLevel,
    required this.goal,
  });

}